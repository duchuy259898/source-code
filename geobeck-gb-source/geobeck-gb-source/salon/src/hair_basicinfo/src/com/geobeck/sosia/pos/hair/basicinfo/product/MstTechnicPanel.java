    /**
     * 選択されたデータを削除する。
     *
     * @return true - 成功
     */
    private boolean delete() {
        boolean result = false;
        MstTechnic mt = null;
        MstUseProducts mup = new MstUseProducts();

        if (0 <= selIndex && selIndex < mtc.size()) {
            mt = mtc.get(selIndex);
        }

        ConnectionWrapper con = SystemInfo.getConnection();

        try {
            mup.setProductDivision(1);


            con.begin();

            if (mt.delete(con) && mup.deleteProductId(con, mt.getTechnicID())) {
                con.commit();
                this.refresh();
                result = true;
                //IVS_LVTu start add 2017/08/15 #21596 [gb] マスタ登録のあと再起動アラートを出す
                isLoadDisplay = true;
                //IVS_LVTu end add 2017/08/15 #21596 [gb] マスタ登録のあと再起動アラートを出す
            } else {
                con.rollback();
            }
        } catch (SQLException e) {
            SystemInfo.getLogger().log(Level.SEVERE, e.getLocalizedMessage(), e);
        }

        return result;
    }

    /**
     * 技術マスタ登録画面用FocusTraversalPolicy
     */
    private class MstTechnicFocusTraversalPolicy
            extends FocusTraversalPolicy {

        /**
         * aComponent のあとでフォーカスを受け取る Component を返します。 aContainer は aComponent
         * のフォーカスサイクルのルートまたはフォーカストラバーサルポリシープロバイダでなければなりません。
         *
         * @param aContainer aComponent のフォーカスサイクルのルートまたはフォーカストラバーサルポリシープロバイダ
         * @param aComponent aContainer のおそらく間接的な子、または aContainer 自体
         * @return aComponent のあとにフォーカスを受け取る Component。適切な Component が見つからない場合は
         * null
         */
        public Component getComponentAfter(Container aContainer,
                Component aComponent) {
            if (aComponent.equals(technicClass)) {
                return technicNo;
            } else if (aComponent.equals(technicNo)) {
                return technicName;
            } else if (aComponent.equals(technicName)) {
                return rdoPraiseTimeLimitEnable;
            } else if (aComponent.equals(rdoPraiseTimeLimitEnable)) {
                return rdoPraiseTimeLimitDisable;
            } else if (aComponent.equals(rdoPraiseTimeLimitDisable)) {
                return praiseTimeLimit;
            } else if (aComponent.equals(praiseTimeLimit)) {
                return price;
            } else if (aComponent.equals(price)) {
                return operationTime;
            } else if (aComponent.equals(operationTime)) {
                return displaySeq;
            } else if (aComponent.equals(displaySeq)) {
                return technicClass;
            }

            return technicClass;
        }

        /**
         * aComponent の前にフォーカスを受け取る Component を返します。 aContainer は aComponent
         * のフォーカスサイクルのルートまたはフォーカストラバーサルポリシープロバイダでなければなりません。
         *
         * @param aContainer aComponent のフォーカスサイクルのルートまたはフォーカストラバーサルポリシープロバイダ
         * @param aComponent aContainer のおそらく間接的な子、または aContainer 自体
         * @return aComponent の前にフォーカスを受け取る Component。適切な Component が見つからない場合は
         * null
         */
        public Component getComponentBefore(Container aContainer,
                Component aComponent) {
            if (aComponent.equals(technicClass)) {
                return technicClass;
            } else if (aComponent.equals(technicNo)) {
                return technicClass;
            } else if (aComponent.equals(technicName)) {
                return technicNo;
            } else if (aComponent.equals(rdoPraiseTimeLimitEnable)) {
                return technicName;
            } else if (aComponent.equals(rdoPraiseTimeLimitDisable)) {
                return rdoPraiseTimeLimitEnable;
            } else if (aComponent.equals(praiseTimeLimit)) {
                return rdoPraiseTimeLimitDisable;
            } else if (aComponent.equals(price)) {
                return praiseTimeLimit;
            } else if (aComponent.equals(operationTime)) {
                return price;
            } else if (aComponent.equals(displaySeq)) {
                return operationTime;
            }

            return technicClass;
        }

        /**
         * トラバーサルサイクルの最初の Component を返します。 このメソッドは、順方向のトラバーサルがラップするときに、次にフォーカスする
         * Component を判定するために使用します。
         *
         * @param aContainer 先頭の Component
         * を返すフォーカスサイクルのルートまたはフォーカストラバーサルポリシープロバイダ
         * @return aContainer のトラバーサルサイクルの先頭の Componet、または適切な Component
         * が見つからない場合は null
         */
        public Component getFirstComponent(Container aContainer) {
            return technicClass;
        }

        /**
         * トラバーサルサイクルの最後の Component を返します。 このメソッドは、逆方向のトラバーサルがラップするときに、次にフォーカスする
         * Component を判定するために使用します。
         *
         * @param aContainer aContainer - 最後の Component
         * を返すフォーカスサイクルのルートまたはフォーカストラバーサルポリシープロバイダ
         * @return aContainer のトラバーサルサイクルの最後の Componet、または適切な Component
         * が見つからない場合は null
         */
        public Component getLastComponent(Container aContainer) {
            return displaySeq;
        }

        /**
         * フォーカスを設定するデフォルトコンポーネントを返します。 aContainer
         * をルートとするフォーカストラバーサルサイクルが新しく開始されたときに、このコンポーネントに最初にフォーカスが設定されます。
         *
         * @param aContainer デフォルトの Component
         * を返すフォーカスサイクルのルートまたはフォーカストラバーサルポリシープロバイダ
         * @return aContainer のトラバーサルサイクルのデフォルトの Componet、または適切な Component
         * が見つからない場合は null
         */
        public Component getDefaultComponent(Container aContainer) {
            return technicClass;
        }

        /**
         * ウィンドウが最初に表示されたときにフォーカスが設定されるコンポーネントを返します。 show() または setVisible(true)
         * の呼び出しで一度ウィンドウが表示されると、 初期化コンポーネントはそれ以降使用されません。
         * 一度別のウィンドウに移ったフォーカスが再び設定された場合、 または、一度非表示状態になったウィンドウが再び表示された場合は、
         * そのウィンドウの最後にフォーカスが設定されたコンポーネントがフォーカス所有者になります。
         * このメソッドのデフォルト実装ではデフォルトコンポーネントを返します。
         *
         * @param window 初期コンポーネントが返されるウィンドウ
         * @return 最初にウィンドウが表示されるときにフォーカス設定されるコンポーネント。適切なコンポーネントがない場合は null
         */
        public Component getInitialComponent(Window window) {
            return technicClass;
        }
    }

