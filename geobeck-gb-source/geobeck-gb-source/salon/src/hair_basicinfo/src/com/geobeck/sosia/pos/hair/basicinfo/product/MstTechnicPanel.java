    /**
     * 入力チェックを行う。
     *
     * @return 入力エラーがなければtrueを返す。
     */
    private boolean checkInput() {
        //技術分類
        if (technicClass.getSelectedIndex() < 0) {
            MessageDialog.showMessageDialog(this,
                    MessageUtil.getMessage(MessageUtil.ERROR_INPUT_EMPTY, "技術分類"),
                    this.getTitle(),
                    JOptionPane.ERROR_MESSAGE);
            technicClass.requestFocusInWindow();
            return false;
        }
        //技術No.
        if (technicNo.getText().equals("")) {
            MessageDialog.showMessageDialog(this,
                    MessageUtil.getMessage(MessageUtil.ERROR_INPUT_EMPTY, "技術No."),
                    this.getTitle(),
                    JOptionPane.ERROR_MESSAGE);
            technicNo.requestFocusInWindow();
            return false;
        }
        //技術名
        if (technicName.getText().equals("")) {
            MessageDialog.showMessageDialog(this,
                    MessageUtil.getMessage(MessageUtil.ERROR_INPUT_EMPTY, "技術名"),
                    this.getTitle(),
                    JOptionPane.ERROR_MESSAGE);
            technicName.requestFocusInWindow();
            return false;
        }

        // 賞美期限
        if (rdoPraiseTimeLimitEnable.isSelected()) {
            try {
                Integer.parseInt(praiseTimeLimit.getText());
            } catch (NumberFormatException e) {
                MessageDialog.showMessageDialog(this,
                        MessageUtil.getMessage(MessageUtil.ERROR_INPUT_EMPTY, "賞美期限"),
                        this.getTitle(),
                        JOptionPane.ERROR_MESSAGE);
            	//IVS_LVTu start edit 2017/08/18  #22255 [gb]技術・商品・コース登録画面 挿入位置がない第1番目の登録ができない。
                praiseTimeLimit.requestFocusInWindow();
            	//IVS_LVTu end edit 2017/08/18 #22255 [gb]技術・商品・コース登録画面 挿入位置がない第1番目の登録ができない。
                return false;
            }
        }

        //売値

            try {
                Long.parseLong(price.getText());
            } catch (Exception e) {
                MessageDialog.showMessageDialog(this,
                        MessageUtil.getMessage(MessageUtil.ERROR_INPUT_WRONG, "売値"),
                        this.getTitle(),
                        JOptionPane.ERROR_MESSAGE);
                price.requestFocusInWindow();
                return false;

            }
        



        //施術時間
        if (operationTime.getText().equals("")) {
            MessageDialog.showMessageDialog(this,
                    MessageUtil.getMessage(MessageUtil.ERROR_INPUT_EMPTY, "施術時間"),
                    this.getTitle(),
                    JOptionPane.ERROR_MESSAGE);
            operationTime.requestFocusInWindow();
            return false;
        } else {
            if (!CheckUtil.isNumber(operationTime.getText())) {
                MessageDialog.showMessageDialog(this,
                        MessageUtil.getMessage(MessageUtil.ERROR_INPUT_WRONG, "施術時間"),
                        this.getTitle(),
                        JOptionPane.ERROR_MESSAGE);
                operationTime.requestFocusInWindow();
                return false;
            }
        }

        //表示順
        if (displaySeq.getText().equals("0")) {
            MessageDialog.showMessageDialog(this,
                    "挿入位置は 1 以上を指定してください。",
                    this.getTitle(),
                    JOptionPane.ERROR_MESSAGE);
            displaySeq.requestFocusInWindow();
            return false;
        }

        return true;
    }

