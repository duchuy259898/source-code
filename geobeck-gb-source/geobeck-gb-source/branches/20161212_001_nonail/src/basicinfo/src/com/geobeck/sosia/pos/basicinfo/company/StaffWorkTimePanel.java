    private boolean helpStaffContains(MstStaff ms) {
        
        boolean result = false;
        
        Iterator itr = swts.iterator();
        while (itr.hasNext()) {
            if (ms.getStaffID().equals(((StaffWorkTime)itr.next()).getStaff().getStaffID())) {
                result = true;
                break;
            }
        }
        
        return result;
    }
    
