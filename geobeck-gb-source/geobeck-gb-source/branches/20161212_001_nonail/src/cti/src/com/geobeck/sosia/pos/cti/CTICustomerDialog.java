                    staffName += (visitRecord.getDesignated() ? " iwj" : " it[j");
                    chargedStaff.setText(staffName);

                    NumberFormat nf = NumberFormat.getInstance();
                    paidLast.setText(nf.format(visitRecord.getSalesTotal()) + " ~");
