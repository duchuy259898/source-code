    private String formatNumberString(Object value) {
        String temp = "";
        if (this.isNumeric(value)) {
            if (this.isDecimal(value)) {
                temp = String.format("%1$,.2f", value);
            } else {
                temp = String.format("%1$,d", value);
            }
        }
        return temp;
    }
    
