    private void init()
    {
        setKeyListener();

        SystemInfo.addMouseCursorChange(closeButton);
        SystemInfo.addMouseCursorChange(renewButton);

        MstShopSetting mss = MstShopSetting.getInstance();
        txtPeriod1.setText(mss.getValidCustomerPeriod1().toString());
        txtPeriod2.setText(mss.getValidCustomerPeriod2().toString());
    }
    
    private void renewButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_renewButtonActionPerformed

        if (txtPeriod1.getText().length() == 0) {
            MessageDialog.showMessageDialog(
                this,
                "期間を入力してください。",
                this.getTitle(),
                JOptionPane.WARNING_MESSAGE);
            txtPeriod1.requestFocusInWindow();
            return;
        }

        if (txtPeriod2.getText().length() == 0) {
            MessageDialog.showMessageDialog(
                this,
                "期間を入力してください。",
                this.getTitle(),
                JOptionPane.WARNING_MESSAGE);
            txtPeriod2.requestFocusInWindow();
            return;
        }
        
        int ret = MessageDialog.showYesNoDialog(
                        this,
                        MessageUtil.getMessage(103),
                        this.getTitle(),
                        JOptionPane.QUESTION_MESSAGE,
                        JOptionPane.NO_OPTION);
                    
        if (ret == JOptionPane.YES_OPTION) {
            this.regist();
            this.close();
        }

    }//GEN-LAST:event_renewButtonActionPerformed
    
    
