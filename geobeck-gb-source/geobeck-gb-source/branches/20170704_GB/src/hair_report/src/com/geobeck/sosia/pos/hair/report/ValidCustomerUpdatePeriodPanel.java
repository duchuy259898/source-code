    private void setKeyListener()
    {
        txtPeriod1.addKeyListener(SystemInfo.getMoveNextField());
        txtPeriod1.addFocusListener(SystemInfo.getSelectText());
        txtPeriod2.addKeyListener(SystemInfo.getMoveNextField());
        txtPeriod2.addFocusListener(SystemInfo.getSelectText());
    }
    
    private void regist()
    {
        MstShopSetting mss = MstShopSetting.getInstance();
        mss.setValidCustomerPeriod1(Integer.valueOf(txtPeriod1.getText()));
        mss.setValidCustomerPeriod2(Integer.valueOf(txtPeriod2.getText()));
        mss.regist();
    }
    
    /**
     * ダイアログを閉じる
     */
    private void close()
    {
        if (this.isDialog()) {
            ((JDialog)this.getParent().getParent().getParent().getParent()).setVisible(false);
        } else {
            this.setVisible(false);
        }
    }
