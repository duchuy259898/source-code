	/**
	 * 初期化処理を行う。
	 */
	private void init()
	{
		if(0 < karteClass.getItemCount())
		{
			karteClass.setSelectedIndex(0);
		}
		if(0 < karteDetailName.getItemCount())
		{
			karteDetailName.setSelectedIndex(0);
		}
		
		this.refresh();
	}
	
	public boolean checkClassRegisted()
	{
		return	(0 < mkcs.size() && 0 < mkc.size());
	}
	
	/**
	 * 表示を更新する。
	 */
	private void refresh()
	{
		if(0 <= karteDetailName.getSelectedIndex())
		{
			mkd	=	(MstKarteDetail)karteDetailName.getSelectedItem();
			
			//カルテ参照マスタをデータベースから読み込む
			try
			{
				ConnectionWrapper	con	=	SystemInfo.getConnection();

				mkd.loadKarteReference(con);
			}
			catch(SQLException e)
			{
				SystemInfo.getLogger().log(Level.SEVERE, e.getLocalizedMessage(), e);
			}
		}
		
		//テーブルにカルテ参照マスタデータを表示する
		this.showData();
		//入力をクリアする
		this.clear();
		
		karteReferenceName.requestFocusInWindow();
	}
    
        /**
         * カルテ詳細名リストを更新する
         */
        private void refreshKarteDetailComboBox()
        {
            // 全ての内容を削除
            karteDetailName.removeAllItems();
            if(0 <= karteClass.getSelectedIndex())
            {
                // カルテ詳細データを取得する
                try
                {
                    ConnectionWrapper	con	=	SystemInfo.getConnection();

                    mkc	=	(MstKarteClass)karteClass.getSelectedItem();
                    mkc.loadKarteDetail(con);

                    con.close();
                }
                catch(SQLException e)
                {
                    SystemInfo.getLogger().log(Level.SEVERE, e.getLocalizedMessage(), e);
                }
                for( MstKarteDetail mkd : mkc )
                {
                        karteDetailName.addItem( mkd ); 
                }
            }
            // フォーカスを取得
            karteDetailName.requestFocusInWindow();
        }
	
	/**
	 * 入力項目をクリアする。
	 */
	private void clear()
	{
		selIndex	=	-1;
		karteReferenceName.setText("");
		displaySeq.setText("");
		
		if(0 < karteReference.getRowCount())
				karteReference.removeRowSelectionInterval(0, karteReference.getRowCount() - 1);
		
		this.changeCurrentData();
	}
	
	/**
	 * データを表示する。
	 */
	private void showData()
	{
		SwingUtil.clearTable(karteReference);
		DefaultTableModel	model	=	(DefaultTableModel)karteReference.getModel();
		
		for(MstKarteReference mkr : mkd)
		{
			Object[]	rowData	=	{	mkr.getKarteReferenceName(),
                                                                mkr.getDisplaySeq()	};
			model.addRow(rowData);
		}
	}
	
	/**
	 * 選択データが変更されたときの処理を行う。
	 */
	private void changeCurrentData()
	{
		int	index	=	karteReference.getSelectedRow();
		
		if(0 <= index && index < mkd.size() && index != selIndex)
		{
			selIndex	=	index;
			
			//選択されているデータを表示
			this.showCurrentData();
		}
		
		this.changeButtonEnabled(0 <= selIndex);
	}
	
	/**
	 * 選択されたデータを入力項目に表示する。
	 */
	private void showCurrentData()
	{
		MstKarteReference	mkr	=	mkd.get(selIndex);
		
		karteReferenceName.setText(mkr.getKarteReferenceName());
		displaySeq.setText(mkr.getDisplaySeq().toString());
	}
	
